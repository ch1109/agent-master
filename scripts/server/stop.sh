#!/bin/bash
#
# Agent Master 停止服务脚本
# 用途：在服务器上停止 PM2 服务
#

set -e
set -u

# ============================================
# 颜色定义
# ============================================
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

# ============================================
# 配置变量
# ============================================
APP_NAME="agent-master"

# ============================================
# 日志函数
# ============================================
log_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1" >&2
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

# ============================================
# 检查 PM2
# ============================================
check_pm2() {
    if ! command -v pm2 >/dev/null 2>&1; then
        log_error "PM2 未安装"
        exit 1
    fi
}

# ============================================
# 停止服务
# ============================================
stop_service() {
    # 检查服务是否在运行
    if ! pm2 list | grep -q "$APP_NAME"; then
        log_warn "服务未运行"
        return 0
    fi

    log_info "正在停止服务..."
    pm2 stop "$APP_NAME"

    # 保存 PM2 进程列表
    pm2 save --force

    log_info "✓ 服务已停止"
}

# ============================================
# 显示状态
# ============================================
show_status() {
    echo ""
    pm2 status "$APP_NAME"
}

# ============================================
# 主函数
# ============================================
main() {
    check_pm2
    stop_service
    show_status
}

main
